export function translateServerField(
  i18n: ServerI18nPayload,
  field: string,
  fallback: string | null | undefined,
  t: ServerTranslate,
): string {
  const ref = i18n?.[field];
  if (!ref) {
    return fallback ?? '';
  }

  const translated = t(ref.key, {
    ...ref.params,
    defaultValue: '',
  });
  return translated && translated !== ref.key ? translated : (fallback ?? '');
}

function buildServerLocale(messages: ServerLocaleMessages): LocaleNode {
  const root: LocaleNode = {};
  for (const key of SERVER_I18N_KEYS) {
    insertLocaleMessage(root, key, messages[key] ?? '');
  }
  return root;
}

function insertLocaleMessage(root: LocaleNode, key: ServerI18nKey, message: string) {
  const parts = key.split('.');
  const localeParts = parts[0] === 'server' ? parts.slice(1) : parts;
  let cursor = root;
  for (const part of localeParts.slice(0, -1)) {
    const next = cursor[part];
    if (typeof next !== 'object' || next === null) {
      cursor[part] = {};
    }
    cursor = cursor[part] as LocaleNode;
  }
  cursor[localeParts[localeParts.length - 1]] = message;
}
